    //metodo para traer datos y cargarlos al combo de candidatos
    public void obtenerCandidatos() {
        this.candidatos = this.controladorCandidato.obtenerCandidatos();
        this.ActualizarComboCandidato(this.candidatos);
    }

    //metodo para actualizacion al combo candidato
    private void ActualizarComboCandidato(LinkedList<ClsCandidato> lista) {

        DefaultComboBoxModel modelo = (DefaultComboBoxModel) this.comboCandidato.getModel();

        modelo.removeAllElements();

        //realiza un recorrrido a los datos a partir del ciclo for 
        for (ClsCandidato c : lista) {

            String candidato = c.getNombre() + '-' + c.getNumeroDocumento();
            modelo.addElement(candidato);

        }
    }

    //metodo para actualizar la tabla elecciones
    public void obtenerElecciones() {
        this.elecciones = this.controlador.obtenerElecciones();
        this.ActualizarTabla(elecciones);
    }

    //metodo para actualizar tabla de candidatos con asociacion
    public void obtenerCandidatoseleccion(String ideleccion) {
        LinkedList<ClsCandidato> listacandidatos = this.controlador.obtenerCandidatoseleccion(ideleccion);
        this.ActualizarTablacandidatos(listacandidatos);
    }

    //metodo para actualizar la tabla donde estan las elecciones
    private void ActualizarTabla(LinkedList<ClsEleccion> lista) {
        DefaultTableModel modelo = (DefaultTableModel) tablaElecciones.getModel();
        modelo.setRowCount(0);

        //realiza un recorrrido a los datos a partir del ciclo for 
        for (ClsEleccion c : lista) {
            Object[] fila = {c.getId(), c.getTipo(), c.getFecha_inicio(), c.getFecha_fin(), c.getEstado()};
            modelo.addRow(fila);
        }
    }

    //metodo para actualizar la tabla donde estan las asociaciones de los candidatos
    private void ActualizarTablacandidatos(LinkedList<ClsCandidato> lista) {
        DefaultTableModel modelo = (DefaultTableModel) this.tablaCandidatos.getModel();
        modelo.setRowCount(0);

        //realiza un recorrrido a los datos a partir del ciclo for 
        for (ClsCandidato c : lista) {
            Object[] fila = {c.getNumeroDocumento(), c.getNombre(), ""};
            modelo.addRow(fila);
        }
    }
    
    //metodo para buscar un tipo de eleccion y editar
    public ClsEleccion BuscarEleccion(String ideleccion){
        for(ClsEleccion eleccion:this.elecciones){
            if(eleccion.getId().equals(ideleccion)){
            return eleccion;
            }
        }
        return null;
    }

