    private void botonAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonAgregarActionPerformed
        // TODO add your handling code here:
        String descripcion = campoDescripcion.getText();
        String eleccion = comboEleccion.getSelectedItem().toString();
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        //parsear al formato sql
        String fecha_inicio = sdf.format(this.FechaInicio.getDate());
        String fecha_fin = sdf.format(this.FechaFin.getDate());

        String estado = comboEstado.getSelectedItem().toString();

        String[] partesfecha = fecha_inicio.split("-", 2);

        String anio = partesfecha[0];

        String ideleccion = anio + eleccion.substring(0, 6);
        //JOptionPane.showMessageDialog(rootPane, date);

        ClsEleccion seleccion = new ClsEleccion(ideleccion, descripcion, eleccion, fecha_inicio, fecha_fin, estado);

        /**
         * preguntar a profesor De date = ic.jDate.getDate(); //ic es la
         * interfaz, jDate el JDatechooser long d = date.getTime(); //guardamos
         * en un long el tiempo java.sql.Date fecha = new java.sql.Date(d);//
         * parseamos al formato del sql at
         */
        ClsMensaje respuesta = this.controlador.agregareleccion(seleccion);
        // ahora verificamos que si se envia correctamente la info muestra un mensaje ok de lo contrario un error
        //luego se actualiza la tabla con candidadtos
        if (respuesta.getTipo().equals(ClsMensaje.OK)) {
            obtenerElecciones();
        }
        JOptionPane.showMessageDialog(rootPane, respuesta.getTexto());
    }//GEN-LAST:event_botonAgregarActionPerformed

